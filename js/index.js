const myfn=()=>{
    var x=document.getElementById('myid')
    var y=document.getElementById('drop_down')
    if(x.style.display=="none"){
        x.style.display="block";
        y.innerHTML="&#x25B2;"
    }
    else{
        x.style.display="none";
        y.innerHTML="&#x25BC;"
    }
    console.log('clicked')
}



